                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>11. RACE
                                                            CANCELLATION</div>
                                                        <div class="answer">
                                                            <p>The race organization reserves the right to cancel or
                                                                cut-short the race for the following reasons:</p>

                                                            <ol>
                                                                <li>Weather warnings sent by the meteorological agency.
                                                                </li>
                                                                <li>The inability to ensure runner safety due to risks
                                                                    such as landslides and rockfalls along the course.
                                                                </li>
                                                                <li>Natural disasters (or declared warnings issued by
                                                                    the “BMKG-Badan Meteorologi and Klimatologi and
                                                                    Earthquake) In such a case, the race course cannot
                                                                    be changed to continue the race, nor will the race
                                                                    be postponed to a later date.</li>
                                                                <li>The decision as to whether or not the race will be
                                                                    cancelled will be made on the day before the race or
                                                                    on the day of the race. This will be communicated
                                                                    via the official race website.</li>
                                                                <li>In the event of an earthquake, hurricane/typhoon,
                                                                    flood, incidents, accidents and/or epidemics, the

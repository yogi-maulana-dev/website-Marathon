                                                <h2 class="title">Rules &amp; Regulations</h2>
                                            </div>
                                        </div>
                                        <div class="column mcb-column mcb-item-e0hsx6zu3 one column_column">
                                            <div class="column_attr clearfix align_justify" style="">The
                                                following rules and regulations apply to all participants of the Lampung Half Marathon 2024 and MUST be adhered to at all times during the event. It is
                                                the responsibility or each runner to read and understand all of the
                                                following before starting the race. All participants are responsible for
                                                their actions. Minimum age for participant is 17 year old on the year of
                                                event. These rules have been established to protect the natural
                                                landscape, participants, volunteers and other individuals. Failure to
                                                comply with these rules may result in: time penalties, immediate
                                                disqualification from the event or fines. The Race Director has the
                                                authority to alter and/or implement rules as needed before or during the
                                                event. Thank you, in advance, for your cooperation and assistance.
                                                Additional rules may follow.
                                                Please read this section very carefully. Any runner not adhering to any
                                                of the following rules is subject to disqualification or penalty.</div>
                                        </div>
                                        <div class="column mcb-column mcb-item-d9pnjml9z one column_accordion">
                                            <div class="accordion">
                                                <div class="mfn-acc accordion_wrapper ">
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>1. ENVIRONMENTAL
                                                            PROTECTION RULES</div>
                                                        <div class="answer">
                                                            <p>We require everyone (runners) to be fully aware and
                                                                follow these rules:</p>

                                                            <p style="text-align:justify";>Sleeping along the course is
                                                                prohibited as it will have a large impact on the
                                                                environment and may also mistakenly lead others to think
                                                                that you require emergency medical assistance. Please
                                                                manage your physical condition carefully and rest/sleep
                                                                at the aid stations.</p>
                                                            <p style="text-align:justify";>You must stay on the trail
                                                                at all times. There will be sections where racers will
                                                                not be able to pass each other in order to protect the
                                                                environment safety matters.</p>

                                                            <p style="text-align:justify";>Picking up or damaging
                                                                plants, animals, fungi and rocks found along the race
                                                                course as well as within the overall race area are
                                                                prohibited.</p>

                                                            <p style="text-align:justify";>Short cutting is strictly
                                                                prohibited due to potential damage to vegetation along
                                                                the course.Please run along the designated course.
                                                                Absolutely no littering along the course.</p>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>2. EMERGENCY
                                                            ASSISTANCE &amp; CONSIDERATION FOR OTHERS</div>
                                                        <div class="answer">
                                                            <p style="text-align:justify";>If during the race, you
                                                                encounter another runner or staff member who is injured
                                                                or sick and unable to move on their own, please give
                                                                priority to assisting that person by telling race staff
                                                                for assistance.</p>

                                                            <p style="text-align:justify";>The race is located on
                                                                national park and other public/private property places.
                                                                Please be mindful and share the trail with others at all
                                                                times. There will be “no passing sections” signs along
                                                                the race course where you will be prohibited from
                                                                passing other runners and hikers.</p>

                                                            <p style="text-align:justify";>Do not damage any property
                                                                located along the race course.</p>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>3. AID STATIONS
                                                            &amp; OUTSIDE SUPPORT</div>
                                                        <div class="answer">
                                                            <ol>
                                                                <li>Food and drinks will be provided as well Aid
                                                                    Stations at (Start, W1, W2) for 21K.</li>
                                                                <li>Outside support by family and friends is not
                                                                    allowed.</li>
                                                                <li>The food and drinks at the aid stations are only for
                                                                    the racers.</li>
                                                                <li>Non-registered racers may not run alongside racers.
                                                                    (No pacers allowed).</li>
                                                            </ol>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>4. MEDICAL
                                                            ASSISTANCE DURING THE RACE</div>
                                                        <div class="answer">
                                                            <ol>
                                                                <li>In order to ensure race safety, race organizer will
                                                                    prepare and arrange for rescue and medical
                                                                    assistance during the race. However, each
                                                                    participant must recognize and understand that there
                                                                    are risks that may result in injuries and/or
                                                                    accidents associated with trail running. You are
                                                                    responsible for yourself and will be participating
                                                                    in this race at your own risk.</li>
                                                                <li>Emergency and first aid tents staffed by a doctor
                                                                    and/or nurse will be located at pointed aid
                                                                    stations. All aid stations will have radio
                                                                    communication to race headquarters.</li>
                                                                <li>Emergency rescues/procedures will be available to
                                                                    racers as well all those involved in the race (e.g.
                                                                    staff, supporters).
                                                                    <ul>
                                                                        <li>Please note that, depending on the area, it
                                                                            may take a long period of time for help to
                                                                            reach you.</li>
                                                                        <li>If you receive emergency medical assistance
                                                                            from a rescue crew and/or doctor, please
                                                                            make sure that you get examined by a medical
                                                                            expert at a hospital after the race. Please
                                                                            contact the race organizer centre to let
                                                                            them know of your condition after the
                                                                            medical examination.</li>
                                                                    </ul>
                                                                </li>
                                                                <li>Doctors and rescue staff have the authority to stop
                                                                    a racer from continuing the race. This will be done
                                                                    by taking off the racer’s number card or BIB.</li>
                                                                <li>If you drop out of the race, you can continuing walk
                                                                    by take a shorter path.</li>
                                                                <li>Depending on your condition, an emergency evacuation
                                                                    may be required. This evacuation will be at the
                                                                    expense of the person being rescued. For all racers
                                                                    must be obtain adequate travel insurance coverage
                                                                    for the duration of your stay in Sibayak –
                                                                    Indonesia.</li>
                                                                <li>In the case of an emergency, if you are unable to
                                                                    reach the nearest Medic aid station, pick up point
                                                                    or finish line, the committee will give assistance.
                                                                </li>
                                                            </ol>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>5. RACE COURSE
                                                            MARKERS</div>
                                                        <div class="answer">
                                                            <ol>
                                                                <li>The race course will be marked using marking tapes
                                                                    and signs. Race staff will also be positioned along
                                                                    the course. Dangerous areas up in the mountains will
                                                                    be marked by ropes and patrolled by staff in order
                                                                    to reduce risk.<br />
                                                                    (Note: Permission has been obtained from the
                                                                    appropriate land owners and property managers to put
                                                                    up temporary signs and marking tapes).</li>
                                                                <li>There will be a “no-passing” section as well as
                                                                    other sections with specific rules/conditions.
                                                                    Please abide by these rules.</li>
                                                                <li>Racers will be stopped at road intersections. Please
                                                                    follow the directions given by the race staff at
                                                                    these intersections.</li>
                                                                <li>The above signs and markers will be set up just
                                                                    before the race and will be immediately removed
                                                                    after the race.</li>
                                                            </ol>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>6. DROPPING OUT
                                                            OF THE RACE</div>
                                                        <div class="answer">
                                                            <p style="text-align:justify";>If you decide to drop out of
                                                                the race, you must notify race staff. Please remove your
                                                                race card/bib and and report to a race staff member so
                                                                that race Organizer Center can be notified. Once you’ve
                                                                dropped out of the race, you will not be able to
                                                                re-start. Remember to keep your race card with you as
                                                                you will need it to retrieve your bags from the bag
                                                                storage area.</p>

                                                            <ul>
                                                                <li>DROPPING OUT AT AN AID STATION<br />
                                                                    Tell an aid station staff that you’ve decided to
                                                                    drop out of the race.
                                                                    You will be able to return to the finish line area
                                                                    by a race shuttle that will stop by the aid
                                                                    stations. This race shuttle will not be available
                                                                    late at night/early morning however, you will be
                                                                    able to rest/sleep in a waiting area provided at the
                                                                    aid stations.</li>

                                                                <li>DROPPING OUT ALONG THE COURSE<br />
                                                                    If you are able to move on your own, please make
                                                                    your way to the closest aid station (either return
                                                                    to the aid station you just left or go the next aid
                                                                    station). If you are unable to move without
                                                                    assistance, please tell staff and follow their
                                                                    instructions. It will take time to get help out to
                                                                    you if you are out on the race course. If you are
                                                                    not feeling well, it would be best to make your
                                                                    decision to drop out at an aid station.</li>
                                                            </ul>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>7. GEAR &amp;
                                                            EQUIPMENT</div>
                                                        <div class="answer">
                                                            <ol>
                                                                <li>Course map (please download and printout the
                                                                    “Overall Course Map” and “Detailed Course Map” from
                                                                    the official website).</li>
                                                                <li>Personal cup for hot/cold drinks at aid stations
                                                                    (minimum 150cc size). Paper cups will not be
                                                                    provided at the aid stations.</li>
                                                                <li>Water (you must carry a maximum of 1,5L of water at
                                                                    the start line.</li>
                                                                <li>Food.</li>
                                                                <li>1 lights with replacement batteries for lights.
                                                                    Battery life will decline in cold temperatures.</li>
                                                                <li>Survival blanket (minimum size of 130cm x 200cm).
                                                                    Whistle.</li>
                                                                <li>Adhesive elastic tape/band (minimum length of 80cm x
                                                                    3cm).</li>
                                                                <li>Warm fleece or warm long sleeve shirt. Cotton shirts
                                                                    are not allowed.</li>
                                                                <li>Warm running pants or leggings or a combination of
                                                                    tights/leggings and long socks which cover the legs
                                                                    completely. Cotton pants/leggings are not allowed.
                                                                </li>
                                                                <li>Warm gloves/mittens and hat that covers your ears.
                                                                </li>
                                                                <li>Waterproof rain jacket with hood & rain pants (both
                                                                    made of waterproof and breathable membrane such as
                                                                    Gore-Tex or something similar; seams must be
                                                                    seam-sealed).</li>
                                                                <li>Gaiters to prevent from sand and gravel insert to
                                                                    your shoes during the summit attack</li>
                                                                <li>First aid kit (such as band aids, disinfectant,
                                                                    etc).</li>
                                                                <li>Travel/accident insurance policy.</li>
                                                            </ol>
                                                        </div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>8. DROP BAGS
                                                        </div>
                                                        <div class="answer">There are no drop bags.</div>
                                                    </div>
                                                    <div class="question">
                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>9. PROHIBITED
                                                            ACTIONS</div>
                                                        <div class="answer">
                                                            <ol>
                                                                <li>Taking shortcuts and/or running off the trails.</li>
                                                                <li>Not carrying the mandatory equipment.</li>
                                                                <li>Throwing garbage onto the course (this applies to
                                                                    both the race and his/her supporters).</li>
                                                                <li>Receiving outside support at other than the
                                                                    stipulated aid stations.</li>
                                                                <li>Running with people who are not officially
                                                                    registered in the race as racers.</li>
                                                                <li>Cheating (e.g. using other means of transportation,
                                                                    using substitute runners, and other unlawful
                                                                    actions).</li>
                                                                <li>Not abiding by the instructions given by official
                                                                    race staff including doctors, nurses, and emergency
                                                                    staff.</li>
                                                                <li>Continuing to run after the cut off times.</li>
                                                                <li>Doping and the use of prohibited drugs.</li>
                                                            </ol>
                                                        </div>
                                                    </div>

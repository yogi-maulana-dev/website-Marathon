                                                        <div class="title"><i class="icon-plus acc-icon-plus"></i><i
                                                                class="icon-minus acc-icon-minus"></i>10. RACER &amp;
                                                            RACE ORGANIZATION RESPONSIBILITIES</div>
                                                        <div class="answer">
                                                            <p>RACER RESPONSIBILITIES</p>

                                                            <ol>
                                                                <li>You understand the unique nature and requirements of
                                                                    competing in an trail running race. You will be
                                                                    running all through mountainous terrain and have
                                                                    undertaken the necessary training to compete in such
                                                                    a race.
                                                                <li>You will be responsible for yourself and have the
                                                                    ability to deal with any problems that may be
                                                                    encountered during the race.
                                                                <li>You are able to deal with, on your own without
                                                                    assistance, deteriorating weather conditions (low
                                                                    temperatures, strong winds and rain ) and other
                                                                    troubles expected when traveling across mountainous
                                                                    terrain.
                                                                <li>You are able to deal with extreme fatigue, internal
                                                                    organ and digestive problems, muscle pain, mild
                                                                    physical injuries and psychological problems on your
                                                                    own
                                                                <li>You are fully aware that when participating in an
                                                                    outdoor activity such as this race, your safety
                                                                    depends on your own skills and abilities.</li>
                                                            </ol>

                                                            <p>RACE ORGANIZATION RESPONSIBILITIES</p>

                                                            <p>The race organization will be responsible for the
                                                                following:</p>

                                                            <ol>
                                                                <li>Tell an aid station staff that you’ve decided to
                                                                    drop out of the race.</li>
                                                                <li>Non-deliberate damage to property by race
                                                                    participants (e.g. plants and animals, objects,
                                                                    trails, etc.)</li>
                                                                <li>Marking and location of signs identifying the race
                                                                    course.</li>
                                                                <li>Race safety management.</li>
                                                            </ol>
                                                        </div>
                                                    </div>
